       $(document).ready(function () {
    $("#kt_new_password_submit").click(function () {
        var invoiceId = $("input[name='receipt_reference']").val();

        $.ajax({
            url: '/receipt/' + invoiceId,
            type: 'GET',
            success: function (response) {
                if (response.success) {
                    // Update the displayed details with full invoice information
                    $("#agencyName").text(response.invoice.agency_name);
                    $("#revenueItemName").text(response.invoice.revenue_item_name);
                    $("#Name").text(response.invoice.taxpayer_name);
                    $("#status").text(response.invoice.status);
                    $("#amount").text(response.invoice.rate);
                    $("#ref").text(response.invoice.transaction_ref);

                    // Show the entire details section
                    $("#invoiceDetailsSection").show();
                } else {
                    // Handle "NOT PAID" message
                    if (response.message === 'NOT PAID') {
                        // Hide all other elements except status
                        $("#Name").closest('h2').hide();
                        $("#revenueItemName").closest('h2').hide();
                        $("#agencyName").closest('h2').hide();
                        $("#amount").closest('h2').hide();
                        $("#ref").closest('h2').hide();

                        // Show only the status
                        $("#status").text('NOT PAID');
                        $("#status").closest('h2').show();
                        $("#invoiceDetailsSection").show();
                    } else if (response.message === 'Invoice not found') {
                        alert('Invoice not found');
                        $("#invoiceDetailsSection").hide(); // Optionally hide the details section
                    }
                }
            },
            error: function (xhr, status, error) {
                alert('Invoice not found');
            }
        });
    });
});

    </script>

